package studentlist1;

public class Student {
  private String name;
  private int age;
  
  public Student(String n, int a)
  { name = n;
    age=a;
  }
  public String getName()
  { return name;
  }
  public int getAge()
  { return age;
  }
 public String toString()
 { return name;
 }
 

  
    
}
